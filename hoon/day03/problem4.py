#문제
'''
실수 점수 하나를 입력받아
반올림한 점수가 90점 이상이면 "합격"
아니면 "불합격"을 출력하시오.
'''

#풀이
a = float(input("점수를 입력하세요: "))
b = round(a)

if b >= 90:
    print("합격")
else:
    print("불합격")

# 파이썬에서 round() 함수 사용 시 주의사항
# "Banker's Rounding"
# 파이썬3에서는 "0.5"처럼 정확히 중간에 있는 숫자를 처리할 때
# 무조건 올리는 것이 아니라 "가장 가까운 짝수" 쪽으로 이동
# 예를 들어, round(2.5)는 2로, round(3.5)는 4로 반올림됨
# 만약 90점 이상이 아니라 90점 초과면 "합격"으로 조건이 바뀐다면,
# 90.5는 합격임에도 90으로 반올림되어 "불합격"이 출력됨