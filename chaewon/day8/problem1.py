n = 0
while n < 5:
    if n == 3:
        break
    print(n)
    n += 1

# 출력 결과는 0 1 2

# while 조건은 언제까지 True인가?
# 답: n이 0부터 시작해서 1씩 커지는데 3일 때 break로 탈출하기 때문에 (조건 자체는 4까지 True이지만 실행은 3에서 멈춤) 답은 n이 0, 1, 2, 3일 때까지 이다.

# break는 언제 실행되는가?
# 답: if n == 3: 조건이 참이 되는 순간 반복문을 중단하고 나가므로 답은 n이 3이 되는 순간 이다.

# print(n)은 몇 번 실행되는가?
# n이 0, 1, 2일 때만 출력하고 3일 때는 출력하기 전에 탈출했기 때문에 답은 총 3번 이다.