# [Day11] 루프 제어 구조
# 📌 문제: 하루 방문자 수 중 최댓값 찾기
# 어떤 서비스의 하루 방문자 수를 여러 번 입력받아,
# 가장 많이 방문한 날의 방문자 수(최댓값)를 출력하시오.
# ________________________________________
# 📥 입력 규칙
# •	방문자 수를 정수로 한 줄씩 입력
# •	총 5번 입력받는다
# ________________________________________
# 📌 처리 조건
# 1️⃣ 입력받은 값은 0 이상 정수여야 한다
# 2️⃣ 만약 음수가 입력되면
# •	"INVALID" 출력
# •	해당 입력은 무시하고 다시 입력 (횟수 차감 ❌)
# 3️⃣ 유효한 값만 비교하여 최댓값을 구한다
# ________________________________________
# 📤 출력
# •	입력이 모두 끝난 뒤
# •	가장 큰 방문자 수 1개 출력
# ________________________________________
# 🔥 구현 포인트 (핵심)
# •	while 또는 for 사용 (둘 중 하나)
# •	if로 값 검증
# •	continue 사용 (잘못된 입력 스킵)
# •	변수 1개로 최댓값 갱신
# ________________________________________

customers_count = []

i = 0
while i < 5:
    daily_customer_num = int(input(f"{i + 1}번째 날 방문자 수 입력 : "))
    if daily_customer_num < 0:
        print("INVALID")
        continue
    customers_count.append(daily_customer_num)
    i += 1

max_num = 0
for i in customers_count:
    if i > max_num:
        max_num = i
print(max_num)
